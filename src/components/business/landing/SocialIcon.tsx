const SocialIcon: React.FC<SocialIconProps> = ({
  platform,
  url,
  style = "default",
  size = "default",
  backgroundColor,
  textColor,
  borderRadius,
  hoverColor,
  borderColor,
  borderWidth,
  hoverBorderColor,
  scale
}) => {
  const { buttonStyles, iconSize } = useSocialIconStyles({ 
    style, 
    size, 
    platform,
    backgroundColor,
    textColor,
    borderRadius,
    hoverColor,
    borderColor,
    borderWidth,
    hoverBorderColor,
    scale
  });

  const renderIcon = () => {
    const iconProps = { className: iconSize };

    switch (platform.toLowerCase()) {
      case "facebook":
        return <FacebookIcon {...iconProps} />;
      case "instagram":
        return <InstagramIcon {...iconProps} />;
      case "twitter":
        return <TwitterIcon {...iconProps} />;
      case "linkedin":
        return <LinkedInIcon {...iconProps} />;
      case "youtube":
        return <YouTubeIcon {...iconProps} />;
      case "tiktok":
        return <TikTokIcon {...iconProps} />;
      case "pinterest":
        return <PinterestIcon {...iconProps} />;
      case "whatsapp":
        return <WhatsAppIcon {...iconProps} />;
      case "website":
      default:
        return <GlobeIcon {...iconProps} />;
    }
  };

  return (
    <a 
      href={url}
      target="_blank"
      rel="noopener noreferrer"
      className={buttonStyles.className}
      style={buttonStyles.style}
      aria-label={`Visit our ${platform}`}
    >
      {renderIcon()}
    </a>
  );
};

export default SocialIcon;
